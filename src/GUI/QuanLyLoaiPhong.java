    public void fillTable() {
        List<LoaiPhong> list = loaiPhongDAO.selectAll();
        fillTable0(list);
    }

    public void fillTable0(List<LoaiPhong> list) {
        DefaultTableModel model = (DefaultTableModel) tblLoaiPhong.getModel();
        model.setRowCount(0);
        try {
            for (LoaiPhong lp : list) {
                Object[] row = {
                    lp.getIdLP(),
                    String.valueOf(lp.getGia()),
                    String.valueOf(lp.getSoNguoi())
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            msg.alert(this, "Lỗi truy vấn dữ liệu!");
        }

    }

    public void fillComboboxSoNguoi() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboSoNguoi.getModel();
        for (int i = 0; i < 20; i++) {
            model.addElement(i + 1);
        }

    }

    private void search() {
        String searchInput = txtSearch.getText();
        try {
            List<LoaiPhong> list = loaiPhongDAO.selectByID(searchInput);
            fillTable0(list);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

