    public LoaiPhong getLoaiPhong() {
        LoaiPhong loaiPhong = new LoaiPhong();
        loaiPhong.setIdLP(txtTenLP.getText());
        loaiPhong.setGia(Float.parseFloat(txtDonGia.getText()));
        loaiPhong.setSoNguoi((int) cboSoNguoi.getSelectedItem());
        return loaiPhong;
    }

    public void insert() {
        try {
            loaiPhongDAO.insert(getLoaiPhong());
            msg.alert(this, "Added!");
            fillTable();
        } catch (Exception e) {
            msg.alert(this, "Fail Added!");
            e.printStackTrace();
        }
    }

    public void delete() {
        try {
            loaiPhongDAO.delete(txtTenLP.getText());
            msg.alert(this, "Deleted!");
            fillTable();
        } catch (Exception e) {
            msg.alert(this, "Fail Delete!");
            e.printStackTrace();
        }
    }

    public void update() {
        try {
            loaiPhongDAO.update(getLoaiPhong());
            msg.alert(this, "Updated!");
            fillTable();
        } catch (Exception e) {
            msg.alert(this, "Fail Update!");
            e.printStackTrace();
        }
    }

    public void displaySelectedRow() {
        System.out.println("Selected Row: " + selectedRowCount);
        tblLoaiPhong.setRowSelectionInterval(selectedRowCount, selectedRowCount);
        txtTenLP.setText(tblLoaiPhong.getValueAt(selectedRowCount, 0).toString());
        txtDonGia.setText(tblLoaiPhong.getValueAt(selectedRowCount, 1).toString());
        cboSoNguoi.setSelectedItem(Integer.valueOf(tblLoaiPhong.getValueAt(selectedRowCount, 2).toString()));
    }

